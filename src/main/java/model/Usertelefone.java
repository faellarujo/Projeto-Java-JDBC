package model;

public class Usertelefone {
	
	private int id;  
	private String numero;  
	private String tipo;
	private int id_userposjava;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getnumero() {
		return numero;
	}
	public void setnumero(String numero) {
		this.numero = numero;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public int getId_userposjava() {
		return id_userposjava;
	}
	public void setId_userposjava(int id_userposjava) {
		this.id_userposjava = id_userposjava;
	}
	
}
